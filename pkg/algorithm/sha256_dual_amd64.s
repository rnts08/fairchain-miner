// func sha256_compress_dual_shani(state1, state2 *[8]uint32, block1, block2 *[64]byte)
TEXT ·sha256_compress_dual_shani(SB), NOSPLIT, $0
    MOVQ state1+0(FP), AX
    MOVQ state2+8(FP), BX
    MOVQ block1+16(FP), CX
    MOVQ block2+24(FP), DX
    LEAQ ·K256(SB), SI

    // Flip Mask
    MOVOU ·SwapMask(SB), X15

    // Block 1 Initialization
    MOVOU (AX), X1
    MOVOU 16(AX), X2
    PSHUFD  $0xB1, X1, X1
    PSHUFD  $0x1B, X2, X2
    MOVOU   X1, X0 // use X0 as temp
    PALIGNR $0x08, X2, X1
    PBLENDW $0xF0, X0, X2
    MOVOU   X1, X14 // Save initial state1 for final addition (interleaved later)
    // Wait! I'll need X14 and another register. I'll use stack for saving initial states.
    SUBQ $64, SP
    MOVOU X1, 0(SP)
    MOVOU X2, 16(SP)

    // Block 2 Initialization
    MOVOU (BX), X8
    MOVOU 16(BX), X9
    PSHUFD  $0xB1, X8, X8
    PSHUFD  $0x1B, X9, X9
    MOVOU   X8, X0
    PALIGNR $0x08, X9, X8
    PBLENDW $0xF0, X0, X9
    MOVOU X8, 32(SP)
    MOVOU X9, 48(SP)

    // Load Message Block 1
    MOVOU (CX), X3; PSHUFB X15, X3
    MOVOU 16(CX), X4; PSHUFB X15, X4
    MOVOU 32(CX), X5; PSHUFB X15, X5
    MOVOU 48(CX), X6; PSHUFB X15, X6

    // Load Message Block 2
    MOVOU (DX), X10; PSHUFB X15, X10
    MOVOU 16(DX), X11; PSHUFB X15, X11
    MOVOU 32(DX), X12; PSHUFB X15, X12
    MOVOU 48(DX), X13; PSHUFB X15, X13

    // Macro for 4 rounds interleaved
    #define ROUNDS_4_DUAL(K_OFF, W1, W2, S1_1, S1_2, S2_1, S2_2) \
        MOVOU W1, X0; PADDD K_OFF(SI), X0; SHA256RNDS2 X0, S1_1, S1_2; \
        MOVOU W2, X0; PADDD K_OFF(SI), X0; SHA256RNDS2 X0, S2_1, S2_2; \
        MOVOU W1, X0; PADDD K_OFF(SI), X0; PSHUFD $0x0E, X0, X0; SHA256RNDS2 X0, S1_2, S1_1; \
        MOVOU W2, X0; PADDD K_OFF(SI), X0; PSHUFD $0x0E, X0, X0; SHA256RNDS2 X0, S2_2, S2_1

    // Rounds 0-3
    ROUNDS_4_DUAL(0, X3, X10, X1, X2, X8, X9)
    // Rounds 4-7
    ROUNDS_4_DUAL(16, X4, X11, X1, X2, X8, X9)
    // Rounds 8-11
    SHA256MSG1 X4, X3; SHA256MSG1 X11, X10
    ROUNDS_4_DUAL(32, X5, X12, X1, X2, X8, X9)
    // Rounds 12-15
    SHA256MSG1 X5, X4; SHA256MSG1 X12, X11
    MOVOU X6, X14; PALIGNR $4, X5, X14; PADDD X14, X3; SHA256MSG2 X6, X3
    MOVOU X13, X14; PALIGNR $4, X12, X14; PADDD X14, X10; SHA256MSG2 X13, X10
    ROUNDS_4_DUAL(48, X6, X13, X1, X2, X8, X9)

    // Rounds 16-19
    SHA256MSG1 X6, X5; SHA256MSG1 X13, X12
    MOVOU X3, X14; PALIGNR $4, X6, X14; PADDD X14, X4; SHA256MSG2 X3, X4
    MOVOU X10, X14; PALIGNR $4, X13, X14; PADDD X14, X11; SHA256MSG2 X10, X11
    ROUNDS_4_DUAL(64, X3, X10, X1, X2, X8, X9)

    // Rounds 20-23
    SHA256MSG1 X3, X6; SHA256MSG1 X10, X13
    MOVOU X4, X14; PALIGNR $4, X3, X14; PADDD X14, X5; SHA256MSG2 X4, X5
    MOVOU X11, X14; PALIGNR $4, X10, X14; PADDD X14, X12; SHA256MSG2 X11, X12
    ROUNDS_4_DUAL(80, X4, X11, X1, X2, X8, X9)

    // Rounds 24-27
    SHA256MSG1 X4, X3; SHA256MSG1 X11, X10
    MOVOU X5, X14; PALIGNR $4, X4, X14; PADDD X14, X6; SHA256MSG2 X5, X6
    MOVOU X12, X14; PALIGNR $4, X11, X14; PADDD X14, X13; SHA256MSG2 X12, X13
    ROUNDS_4_DUAL(96, X5, X12, X1, X2, X8, X9)

    // Rounds 28-31
    SHA256MSG1 X5, X4; SHA256MSG1 X12, X11
    MOVOU X6, X14; PALIGNR $4, X5, X14; PADDD X14, X3; SHA256MSG2 X6, X3
    MOVOU X13, X14; PALIGNR $4, X12, X14; PADDD X14, X10; SHA256MSG2 X13, X10
    ROUNDS_4_DUAL(112, X6, X13, X1, X2, X8, X9)

    // Rounds 32-35
    SHA256MSG1 X6, X5; SHA256MSG1 X13, X12
    MOVOU X3, X14; PALIGNR $4, X6, X14; PADDD X14, X4; SHA256MSG2 X3, X4
    MOVOU X10, X14; PALIGNR $4, X13, X14; PADDD X14, X11; SHA256MSG2 X10, X11
    ROUNDS_4_DUAL(128, X3, X10, X1, X2, X8, X9)

    // Rounds 36-39
    SHA256MSG1 X3, X6; SHA256MSG1 X10, X13
    MOVOU X4, X14; PALIGNR $4, X3, X14; PADDD X14, X5; SHA256MSG2 X4, X5
    MOVOU X11, X14; PALIGNR $4, X10, X14; PADDD X14, X12; SHA256MSG2 X11, X12
    ROUNDS_4_DUAL(144, X4, X11, X1, X2, X8, X9)

    // Rounds 40-43
    SHA256MSG1 X4, X3; SHA256MSG1 X11, X10
    MOVOU X5, X14; PALIGNR $4, X4, X14; PADDD X14, X6; SHA256MSG2 X5, X6
    MOVOU X12, X14; PALIGNR $4, X11, X14; PADDD X14, X13; SHA256MSG2 X12, X13
    ROUNDS_4_DUAL(160, X5, X12, X1, X2, X8, X9)

    // Rounds 44-47
    SHA256MSG1 X5, X4; SHA256MSG1 X12, X11
    MOVOU X6, X14; PALIGNR $4, X5, X14; PADDD X14, X3; SHA256MSG2 X6, X3
    MOVOU X13, X14; PALIGNR $4, X12, X14; PADDD X14, X10; SHA256MSG2 X13, X10
    ROUNDS_4_DUAL(176, X6, X13, X1, X2, X8, X9)

    // Rounds 48-51
    SHA256MSG1 X6, X5; SHA256MSG1 X13, X12
    MOVOU X3, X14; PALIGNR $4, X6, X14; PADDD X14, X4; SHA256MSG2 X3, X4
    MOVOU X10, X14; PALIGNR $4, X13, X14; PADDD X14, X11; SHA256MSG2 X10, X11
    ROUNDS_4_DUAL(192, X3, X10, X1, X2, X8, X9)

    // Rounds 52-55
    SHA256MSG1 X3, X6; SHA256MSG1 X10, X13
    MOVOU X4, X14; PALIGNR $4, X3, X14; PADDD X14, X5; SHA256MSG2 X4, X5
    MOVOU X11, X14; PALIGNR $4, X10, X14; PADDD X14, X12; SHA256MSG2 X11, X12
    ROUNDS_4_DUAL(208, X4, X11, X1, X2, X8, X9)

    // Rounds 56-59
    MOVOU X5, X14; PALIGNR $4, X4, X14; PADDD X14, X6; SHA256MSG2 X5, X6
    MOVOU X12, X14; PALIGNR $4, X11, X14; PADDD X14, X13; SHA256MSG2 X12, X13
    ROUNDS_4_DUAL(224, X5, X12, X1, X2, X8, X9)

    // Rounds 60-63
    ROUNDS_4_DUAL(240, X6, X13, X1, X2, X8, X9)

    // Final Addition Block 1
    PADDD 0(SP), X1
    PADDD 16(SP), X2
    PSHUFD  $0x1B, X1, X1
    PSHUFD  $0xB1, X2, X2
    MOVOU   X1, X0
    PBLENDW $0xF0, X2, X1
    PALIGNR $0x08, X0, X2
    MOVOU X1, (AX)
    MOVOU X2, 16(AX)

    // Final Addition Block 2
    PADDD 32(SP), X8
    PADDD 48(SP), X9
    PSHUFD  $0x1B, X8, X8
    PSHUFD  $0xB1, X9, X9
    MOVOU   X8, X0
    PBLENDW $0xF0, X9, X8
    PALIGNR $0x08, X0, X9
    MOVOU X8, (BX)
    MOVOU X9, 16(BX)

    ADDQ $64, SP
    VZEROUPPER
    RET
